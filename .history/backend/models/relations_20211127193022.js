const { User } = require("../models/User");
const { Post } = require("../models/Post");
const { Comment } = require("../models/Comment");

User.hasMany(Post, {
    foreignKey: {
        type: DataTypes.UUID,
        allowNull: false
    }
});
Post.belongsTo(User);

Post.hasMany(Comment, {
    foreignKey: {
        type: DataTypes.UUID,
        allowNull: false
    }
});
Comment.belongsTo(Post);